// Set constants for height
const uint16_t ScreenHeight = 240;
const uint16_t ScreenWidth = 320;

// Score and life
uint16_t score = 0;
uint16_t life = 3;

// Enemy 
struct Enemy {
	uint32_t x, y, deathTimer;
	bool dead;
};

// Bullet
struct Bullet {
	uint32_t x, y;
	bool fired, draw;
};

// Player (only needs x coord)
uint16_t Player_x = ScreenWidth/2;

struct Enemy enemies[8];

struct Bullet bullets[3];

const uint32_t BlinkRate = 500000; 
uint16_t blinkTimer = 0;

int16_t player_direction;
bool buttonPressed, blinkLEDs;

// 3 tasks:  I/O, Update display, Game logic
// use mutex to control updating of positions. Mutex is between game logic thread and display thread -> add this later

void input(void const *arg) {
	LPC_GPIO2->FIODIR |= 0x0000007C;
	LPC_GPIO1->FIODIR |= 0xB0000000;
	
	uint32_t LEDTimer = 0;

	while(1){
		// get player controls:
		if (!(LPC_GPIO1->FIOPIN & (1 << 26)))  player_direction = -1; // left
		else if (!(LPC_GPIO1->FIOPIN & (1 << 24))) player_direction = 1; // right
		else player_direction = 0;
		
		if (!(LPC_GPIO1->FIOPIN & (1 << 20))) buttonPressed = true;
		else buttonPressed = false;
		
		// Blink LEDs at BlinkRate
		if (blinkLEDs) {
			if (LEDTimer > BlinkRate) LEDTimer = 0;
			else if (LEDTimer > BlinkRate/2) {
				// turn on leds
				LPC_GPIO2->FIODIR &= !(0x0000007C);
				LPC_GPIO1->FIODIR &= !(0xB0000000);
			}
			else {
				// turn off leds
				LPC_GPIO2->FIODIR |= 0x0000007C;
				LPC_GPIO1->FIODIR |= 0xB0000000;
			}
			LEDTimer++;
		}
		if (blinkTimer == 0){
			// turn off leds
			LPC_GPIO2->FIODIR |= 0x0000007C;
			LPC_GPIO1->FIODIR |= 0xB0000000;
		}
	}
}

void updateDisplay(void const *arg) {
	// Initializes GLCD for display
	GLCD_Init();
	GLCD_Clear(0x0000);
	GLCD_SetBackColor(0x0000);
	GLCD_SetTextColor(White);

	// Char arrays to update score and life
	char scoreText[32];
	char livesText[32];

	// Display Loop
	while(1){
		// Iterates bullet array and updates of bullets location on screen
		for (int i = 0; i < 3; i++) {
			if (bullets[i].fired){
				GLCD_Bitmap(bullets[i].x+10, bullets[i].y, 16, 24, (unsigned char*)bullet_bitmap);
				GLCD_Bitmap(bullets[i].x+10, bullets[i].y-48, 32, 48, (unsigned char*)black_box_bitmap);
			}
		}
		// Iterates enemy array and updates enemy location on screen
		for(int i = 0; i < 8; i++) {
			// If enemy is dead, puts enemy at top of the screen
			if (enemies[i].dead){
				GLCD_Bitmap(enemies[i].x, enemies[i].y, 32, 48, (unsigned char*)black_box_bitmap);
				GLCD_Bitmap(enemies[i].x, enemies[i].y-40, 32, 48, (unsigned char*)black_box_bitmap);

				enemies[i].dead = false;
			}
			// If deathtimer is less than or equal to 1
			else if (enemies[i].deathTimer <= 1){
				GLCD_Bitmap(enemies[i].x, enemies[i].y, 32, 36, (unsigned char*)enemy_bitmap);
			}
		}
		
		// Prints score and life onto the screen
		sprintf(scoreText, "score: %d", score);
		sprintf(livesText, "lives: %d", life);
		GLCD_DisplayString(9, 1, 1, (unsigned char *)scoreText);
		GLCD_DisplayString(9, 11, 1, (unsigned char *)livesText);
		
		GLCD_Bitmap(Player_x, 0, 64, 24, (unsigned char*)player_bitmap); 
		//GLCD_DisplayString(0, 0, 1, (unsigned char*)score_text);
		
		if (life == 0)
			break;
	}
	
	// When game is over
	GLCD_Clear(0x0000);
	GLCD_DisplayString(5, 5, 1, (unsigned char *)"GAME OVER");
	GLCD_DisplayString(8, 5, 1, (unsigned char *)scoreText);
	
	// Blink LEDs constantly to indicate game is over
	blinkLEDs = true;
	osDelay(100000);
	blinkLEDs = false;

}

void gameLogic(void const *arg) {
	blinkLEDs = false;
	
	uint16_t enemySpeed = 1;
	uint16_t enemyDeathTime = 500;
	uint16_t fireRateTimer = 0;

	uint16_t bulletIterator = 0;
	
	// set all enemies to dead on start -> have them spawn at different times
	for(int i = 0; i < 8; i++) {
		enemies[i].deathTimer = i*200 + 300;
		enemies[i].y = ScreenHeight;
		enemies[i].x = (uint32_t)(lfsr113() >> 24) + 20;
	}
	
	while(1){
		// Updates player position based on inputs
		Player_x += player_direction*2*(Player_x + player_direction*2 > 0 && Player_x + player_direction*2 < ScreenWidth - 30);
		
			
		// move the enemies
		for(int i = 0; i < 8; i++) {
			// enemy is dead: skip to next
			if (enemies[i].deathTimer > 1){
				enemies[i].deathTimer --;
				if (enemies[i].deathTimer < enemyDeathTime/2) {
					enemies[i].y = ScreenHeight;
					enemies[i].x = (uint32_t)(lfsr113() >> 24) + 20;
				}
				continue;
			}
			
			enemies[i].y -= enemySpeed;
			
			// enemy reached bottom of screen
			if (enemies[i].y < 10) {
				life--;
				blinkLEDs = true;
				blinkTimer = 100;
				enemies[i].dead = true;
				enemies[i].y = 0;
				enemies[i].deathTimer = enemyDeathTime;
			}
			
			// enemy collides with bullet
			for (int j = 0; j < 3; j++) {
				if(!bullets[j].fired)
					continue;
				if(bullets[j].x > enemies[i].x - 20 && bullets[j].x < enemies[i].x + 20 && bullets[j].y > enemies[i].y - 16 && bullets[j].y < enemies[i].y + 16){
					score++;
					if (enemyDeathTime > 30)
						enemyDeathTime -= 10;
					enemies[i].dead = true;
					enemies[i].deathTimer = enemyDeathTime;
					bullets[j].fired = false;
				}
			}
			
		}
		
		// blink LED timer logic
		if (blinkLEDs) {
			blinkTimer --;
			if (blinkTimer == 0)
				blinkLEDs = false;
		}
		
		// fire rate timer to limit fire rate
		if (fireRateTimer > 0){
			fireRateTimer--;
		}
		
		// move the bullets
		if (buttonPressed && fireRateTimer == 0) {
			fireRateTimer = 50;
			bullets[bulletIterator].fired = true;
			bullets[bulletIterator].y = 0;
			bullets[bulletIterator].x = Player_x + 16;

			bulletIterator++;
			if (bulletIterator >= 3)
				bulletIterator = 0;
		}
	
		// Updates positions of bullets as they are fired
		for (int i = 0; i < 3; i++) {
			if (bullets[i].fired){
				bullets[i].y += 5;
			}
			if (bullets[i].y > ScreenHeight + 10) {
				bullets[i].fired = false;
			}
		}
		
		if (life == 0)
			break;
		
		osDelay(100);
	}
}

// Thread definitions
osThreadDef(input, osPriorityNormal, 1, 0);
osThreadDef(updateDisplay, osPriorityNormal, 1, 0);
osThreadDef(gameLogic, osPriorityNormal, 1, 0);


