// 3 tasks:  I/O, Update display, Game logic
// use mutex to control updating of positions. Mutex is between game logic thread and display thread -> add this later

void input(void const *arg) {
	LPC_GPIO2->FIODIR |= 0x0000007C;
	LPC_GPIO1->FIODIR |= 0xB0000000;
	
	uint32_t LEDTimer = 0;

	while(1){
		// get player controls:
		if (!(LPC_GPIO1->FIOPIN & (1 << 23)))  player_direction = -1; // left
		else if (!(LPC_GPIO1->FIOPIN & (1 << 25))) player_direction = 1; // right
		else player_direction = 0;
		
		if (!(LPC_GPIO1->FIOPIN & (1 << 20))) buttonPressed = true;
		else buttonPressed = false;
		
		// Blink LEDs at BlinkRate
		if (blinkLEDs) {
			if (LEDTimer > BlinkRate) LEDTimer = 0;
			else if (LEDTimer > BlinkRate/2) {
				// turn on leds
				LPC_GPIO2->FIODIR &= !(0x0000007C);
				LPC_GPIO1->FIODIR &= !(0xB0000000);
			}
			else {
				// turn off leds
				LPC_GPIO2->FIODIR |= 0x0000007C;
				LPC_GPIO1->FIODIR |= 0xB0000000;
			}
			LEDTimer++;
		}
		//printf("%d \n", LEDTimer);
	}
}

void updateDisplay(void const *arg) {
	GLCD_Init();
	GLCD_Clear(0x0000);
	
	struct Bullet* bullet_itr = (struct Bullet*)malloc(sizeof(struct Bullet));

	while(1){
		bullet_itr = head;

		for(int i = 0; i < 8; i++) {
			if (enemies[i].dead){
				GLCD_Bitmap(enemies[i].x, enemies[i].y, 32, 48, (unsigned char*)black_box_bitmap);
				enemies[i].dead = false;
			}
			else if (enemies[i].deathTimer <= 1){
				GLCD_Bitmap(enemies[i].x, enemies[i].y, 32, 24, (unsigned char*)enemy_bitmap);
			}
		}
		
		//update bullets on screen 
		while(bullet_itr->next) {
			GLCD_Bitmap(bullet_itr->x, bullet_itr->y, 32, 24, (unsigned char*)bullet_bitmap);
			bullet_itr = bullet_itr->next;
		}

		GLCD_Bitmap(Player_x, 0, 32, 24, (unsigned char*)player_bitmap); 
		
		
		osDelay(100);
		
	}
	
	
}

